    private void messageTextAreaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_messageTextAreaKeyTyped
        if (messageTextArea.getText().equals("\n")) {
            messageTextArea.setText("");
        }
    }//GEN-LAST:event_messageTextAreaKeyTyped

    private void sendMessage() {
        String message = messageTextArea.getText();

        if (message.isEmpty()) {
            messageTextArea.setText("");
            return;
        }

        Client client = Application.getClient();

        Application.send(new Message(
                client.getUser().username, "Send",
                client.getAuthtoken(), Arrays.asList(username),
                Parameters.of("content", message)));

        append(message, client.getUser().username, new Date());

        messageTextArea.setText("");
    }

