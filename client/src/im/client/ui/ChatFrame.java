    private String username;

    public void setUsername(String username) {
        this.username = username;
        setTitle(username + " - IM Chat");
        usernameLabel.setText(username);
    }

    public void append(Message message) {
        append(message.parameters.get("content"), message.sender, message.date);
    }

    public void append(String message, String uesr, Date date) {
        conversationTextArea.append(String.format("%s - %s: %s\n", uesr, DateFormat.getTimeInstance(DateFormat.SHORT).format(date), message));
    }

    public void append(String message) {
        conversationTextArea.append(
                message + "\n");
    }
